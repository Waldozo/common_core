#include "ft_printf.h"

int	ft_strlen(char *str)
{
	int	i;
	i = 0;
	while(str[i])
	{
		i++;
	}
	return(i);
}
int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		ft_putstr("(null)");
		return (6);
	}
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
